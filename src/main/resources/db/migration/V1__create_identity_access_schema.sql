CREATE TABLE IF NOT EXISTS USUARIO_IDENTIDAD (
  ID BIGINT AUTO_INCREMENT NOT NULL COMMENT 'Identificador interno de identidad',
  UUID_USUARIO CHAR(36) NOT NULL COMMENT 'Identificador global de usuario/actor',
  TIPO_ACTOR VARCHAR(15) NOT NULL COMMENT 'CLIENTE, EMPLEADO o SERVICIO',
  USERNAME VARCHAR(80) NOT NULL COMMENT 'Nombre de usuario unico',
  EMAIL VARCHAR(160) NULL COMMENT 'Correo principal verificable',
  EMAIL_VERIFICADO TINYINT(1) NOT NULL DEFAULT 0 COMMENT '1=correo verificado',
  TELEFONO_MOVIL VARCHAR(25) NULL COMMENT 'Telefono para MFA/notificaciones',
  PASSWORD_HASH VARCHAR(255) NULL COMMENT 'Hash fuerte de password; null en cuentas tecnicas sin login humano',
  PASSWORD_ALGORITHM VARCHAR(30) NULL DEFAULT 'BCrypt' COMMENT 'Algoritmo usado: BCrypt/Argon2id',
  PASSWORD_UPDATED_AT DATETIME NULL COMMENT 'Ultimo cambio de password',
  REQUIERE_CAMBIO_PASSWORD TINYINT(1) NOT NULL DEFAULT 0 COMMENT 'Obliga cambio de clave en siguiente login',
  ESTADO VARCHAR(20) NOT NULL DEFAULT 'ACTIVO' COMMENT 'Estado de la identidad',
  INTENTOS_FALLIDOS INT NOT NULL DEFAULT 0 COMMENT 'Intentos fallidos consecutivos',
  BLOQUEADO_HASTA DATETIME NULL COMMENT 'Bloqueo temporal por seguridad',
  ULTIMO_LOGIN DATETIME NULL COMMENT 'Ultimo login exitoso',
  UUID_REFERENCIA_EXTERNA CHAR(36) NULL COMMENT 'UUID de cliente/empleado en microservicio propietario; sin FK cruzada',
  REFERENCIA_TIPO VARCHAR(30) NULL COMMENT 'CUSTOMER, CORE_USER, SERVICE, etc.',
  FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  VERSION INT NOT NULL DEFAULT 0 COMMENT 'Version para concurrencia optimista',
  CONSTRAINT PK_USUARIO_IDENTIDAD PRIMARY KEY (ID),
  CONSTRAINT UK_USUARIO_IDENTIDAD_UUID UNIQUE (UUID_USUARIO),
  CONSTRAINT UK_USUARIO_IDENTIDAD_USERNAME UNIQUE (USERNAME),
  CONSTRAINT UK_USUARIO_IDENTIDAD_EMAIL UNIQUE (EMAIL),
  CONSTRAINT CK_USUARIO_IDENTIDAD_TIPO CHECK (TIPO_ACTOR IN ('CLIENTE','EMPLEADO','SERVICIO')),
  CONSTRAINT CK_USUARIO_IDENTIDAD_ESTADO CHECK (ESTADO IN ('ACTIVO','INACTIVO','BLOQUEADO','PENDIENTE','REVOCADO'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Identidades autenticables humanas o tecnicas';

