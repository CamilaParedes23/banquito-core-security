CREATE OR REPLACE VIEW V_USUARIO_PERMISOS AS
SELECT u.UUID_USUARIO, u.USERNAME, r.CODIGO AS ROL, p.CODIGO AS PERMISO
FROM USUARIO_IDENTIDAD u
JOIN USUARIO_ROL ur ON ur.USUARIO_ID = u.ID AND ur.ESTADO = 'ACTIVO'
JOIN ROL r ON r.ID = ur.ROL_ID AND r.ESTADO = 'ACTIVO'
JOIN ROL_PERMISO rp ON rp.ROL_ID = r.ID AND rp.ESTADO = 'ACTIVO'
JOIN PERMISO p ON p.ID = rp.PERMISO_ID AND p.ESTADO = 'ACTIVO';

CREATE INDEX IDX_USUARIO_ESTADO ON USUARIO_IDENTIDAD(ESTADO);
CREATE INDEX IDX_AUDITORIA_SEG_FECHA ON AUDITORIA_SEGURIDAD(FECHA_EVENTO);
CREATE INDEX IDX_OUTBOX_IDENTITY_ESTADO ON OUTBOX_EVENT(ESTADO, FECHA_CREACION);
